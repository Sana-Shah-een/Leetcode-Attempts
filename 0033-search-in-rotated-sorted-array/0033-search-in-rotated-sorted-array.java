class Solution {
    public int search(int[] nums, int target) {
        int left=0;
        int right=nums.length-1;
        while(left<=right){
            int mid = (left+(right-left)/2);
            if(target==nums[mid]){
                return mid;
            }
            //left half sorted 
            if(nums[left]<=nums[mid]){
                //check whether target is in left sorted half or not
                if(target>=nums[left] && target<nums[mid]){
                    right = mid-1;
                }else{
                    left = mid+1;
                }
            }

            //right half sorted
            else{
                //check whether target exists in right half sorted array or not
                if(target>mid && target<=nums[right]){
                    left = mid+1;
                }
                else{
                    right = mid-1;
                }
            }
        }
        return -1;
    }
}